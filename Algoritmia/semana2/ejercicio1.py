# Escriba programa en Python que lea dos datos numéricos reales y que luego los imprima en una sola línea separados por la palabra “and”.
# Ejemplo:
# Entradas:
# 8.2
# -100.98
# Salida:
# 8.2 and -100.98

number_1 = float(input("Input the first number: "))
number_2 = float(input("Input the second number: "))
print(str(number_1)+" and "+str(number_2))